    /**
     * Saves the modifications made to the employee in the DB, if there is no data inconsistency 
     */
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = txtEmpName.getText();
        int nameSize = name.length();
        
        String cpf = txtEmpCPF.getText();
        int cpfSize = cpf.length();
        
        String phone = txtEmpPhone.getText();
        int phoneSize = phone.length();
        
        String oldPwd = String.valueOf(txtOldPwd.getPassword());
        int oldPwdSize = oldPwd.length();
        
        String newPdw = String.valueOf(txtNewPwd.getPassword());
        int newPdwSize = newPdw.length();
        
        try {
            if(!((nameSize > 0 && nameSize <= 50) && (cpfSize > 0 && cpfSize <= 11) &&
                 (phoneSize > 0 && phoneSize <= 11) && (oldPwdSize > 0 && oldPwdSize <= 20) &&
                 (newPdwSize > 0 && newPdwSize <= 20)))
            {
                throw new Exception("Algum dos campos preenchidos está inválido!");
            }
            
            if(!emp.getSenha().equals(oldPwd))
                throw new Exception("Senha antiga é inválida!");
            
            emp.setNome(name);
            emp.setCpf(cpf);
            emp.setTelefone(phone);
            emp.setSenha(newPdw);
            
            new FuncionarioDao().salvar(emp);
            
            dispose();            
        } 
        catch (Exception e) // Pop up a error message if something went wrong
        { 
            errorDialogue.setSize(errorDialogue.getPreferredSize());
            errorDialogue.setLocationRelativeTo(this);
            lblErrorMsg.setText(e.getMessage());
            errorDialogue.setVisible(true);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

